package com.mycompany.refeitorio;

public class TAE {

    private String siape;
    private String name;

    public TAE(String siape, String name) {
        this.siape = siape;
        this.name = name;
    }

    public String getSiape() {
        return siape;
    }

    public void setSiape(String siape) {
        this.siape = siape;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

}
